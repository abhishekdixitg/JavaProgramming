package M4;

public class Student {
	private int id;
	private String name;
	private int marks;

	public int getId() {
		return id;
	}

	public int getMarks() {
		return marks;
	}

	public String getName() {
		return name;
	}

	public void setId(int id) {
		this.id = id;
	}

	public void setMarks(int marks) {
		this.marks = marks;
	}

	public void setName(String name) {
		this.name = name;
	}

}
